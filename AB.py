metrics = ['paid_ctr', 'organic_ctr', 'rpm', 'rps', 'views_per_session']
for metric in metrics:
    t_stat, p_value = perform_t_test(group_a, group_b, metric)
    print(f"\nüìä –¢–µ—Å—Ç –¥–ª—è –º–µ—Ç—Ä–∏–∫–∏ {metric}:")
