import pandas as pd
from scipy import stats
import seaborn as sns
import matplotlib.pyplot as plt
from scipy import stats

# –ó–∞–≥—Ä—É–∑–∫–∞ –∏ –ø—Ä–æ—Å–º–æ—Ç—Ä –¥–∞–Ω–Ω—ã—Ö
file_path = r'C:\Users\user\Desktop\project-ab\marketpele_ab_test.xlsx' # –∑–∞–≥—Ä—É–∂–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ —Ñ–∞–π–ª–∞ Excel
data = pd.read_excel(file_path)

# –ü—Ä–µ–¥–≤–∞—Ä–∏—Ç–µ–ª—å–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –¥–∞–Ω–Ω—ã—Ö
print("\n–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–æ–ø—É—Å–∫–æ–≤ –¥–∞–Ω–Ω—ã—Ö –≤ –∫–∞–∂–¥–æ–º —Å—Ç–æ–ª–±—Ü–µ:") 
print(data.isnull().sum())  # –ø—Ä–æ–≤–µ—Ä—è–µ–º, —Å–∫–æ–ª—å–∫–æ –ø—Ä–æ–ø—É—Å–∫–æ–≤ –≤ –∫–∞–∂–¥–æ–º —Å—Ç–æ–ª–±—Ü–µ
print("\n–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥—É–±–ª–∏–∫–∞—Ç–æ–≤ –¥–∞–Ω–Ω—ã—Ö:")
print(data.duplicated().sum())  # –ø—Ä–æ–≤–µ—Ä—è–µ–º, —Å–∫–æ–ª—å–∫–æ –¥—É–±–ª–∏–∫–∞—Ç–æ–≤ –≤ –¥–∞–Ω–Ω—ã—Ö

data = data.drop_duplicates() # —É–¥–∞–ª—è–µ–º –¥—É–±–ª–∏–∫–∞—Ç—ã –¥–∞–Ω–Ω—ã—Ö (–µ—Å–ª–∏ –æ–Ω–∏ –µ—Å—Ç—å)

if 'date' in data.columns:
    data['date'] = pd.to_datetime(data['date']) # –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç–æ–ª–±–µ—Ü —Å –¥–∞—Ç–æ–π –≤ —Ñ–æ—Ä–º–∞—Ç datetime

print("\n–ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–Ω–Ω—ã—Ö –ø–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏:") # –ø—Ä–æ–≤–µ—Ä—è–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏
print(data.head())

# –ü—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã–π –≤—ã–≤–æ–¥
print("\n–î–∞–Ω–Ω—ã–µ –∑–∞–≥—Ä—É–∂–µ–Ω—ã –∏ –æ—á–∏—â–µ–Ω—ã. –î–æ–±–∞–≤–ª–µ–Ω—ã —Ä–∞—Å—á–µ—Ç–Ω—ã–µ –º–µ—Ç—Ä–∏–∫–∏: Paid CTR, Organic CTR, RPM, RPS, Views per session")

# –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –º–µ—Ç—Ä–∏–∫
data['paid_ctr'] = data['sponsord_clicks'] / data['pageviews'] # –≤—ã—á–∏—Å–ª—è–µ–º Paid CTR (Sponsored Click-Through Rate)

data['organic_ctr'] = data['organic_clicks'] / data['pageviews'] # –≤—ã—á–∏—Å–ª—è–µ–º Organic CTR (Organic Click-Through Rate)

data['rpm'] = (data['revenue'] / data['pageviews']) * 1000 # –≤—ã—á–∏—Å–ª—è–µ–º RPM (Revenue per Mille)

data['rps'] = (data['revenue'] / data['sessions']) * 1000 # –≤—ã—á–∏—Å–ª—è–µ–º RPS (Revenue per Session)

data['views_per_session'] = data['pageviews'] / data['sessions'] # –≤—ã—á–∏—Å–ª—è–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–æ—Å–º–æ—Ç—Ä–æ–≤ –∑–∞ —Å–µ—Å—Å–∏—é

print("\n–†–∞—Å—á–µ—Ç –º–µ—Ç—Ä–∏–∫:")
print(data[['paid_ctr', 'organic_ctr', 'rpm', 'rps', 'views_per_session']].head())

# –°—Ä–∞–≤–Ω–∏—Ç–µ–ª—å–Ω—ã–π –∞–Ω–∞–ª–∏–∑
grouped_data = data.groupby('group_name').agg({
    'paid_ctr': 'mean',
    'organic_ctr': 'mean',
    'rpm': 'mean',
    'rps': 'mean',
    'views_per_session': 'mean'
}).reset_index() # –¥–µ–ª–∞–µ–º –≥—Ä—É–ø–ø–∏—Ä–æ–≤–∫—É –ø–æ 'group_name' –∏ –≤—ã—á–∏—Å–ª—è–µ–º —Å—Ä–µ–¥–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è –¥–ª—è –∫–∞–∂–¥–æ–π –≥—Ä—É–ø–ø—ã

print("\n–†–∞—Å—á–µ—Ç —Å—Ä–µ–¥–Ω–∏—Ö –∑–Ω–∞—á–µ–Ω–∏–π –¥–ª—è –∫–∞–∂–¥–æ–π –≥—Ä—É–ø–ø—ã:")
print(grouped_data)

# –°—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ (t-—Ç–µ—Å—Ç)
group_a = data[data['group_name'] == 'A'] # —Ä–∞–∑–¥–µ–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –Ω–∞ –≥—Ä—É–ø–ø—ã A –∏ B
group_b = data[data['group_name'] == 'B']

def perform_t_test(group_a, group_b, metric): # –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è t-—Ç–µ—Å—Ç–∞ –∏ –≤—ã–≤–æ–¥–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    t_stat, p_value = stats.ttest_ind(group_a[metric], group_b[metric], nan_policy='omit')
    return t_stat, p_value 

# –í–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏—è —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –º–µ—Ç—Ä–∏–∫ –ø–æ –≥—Ä—É–ø–ø–∞–º
metrics = ['paid_ctr', 'organic_ctr', 'rpm', 'rps', 'views_per_session']

